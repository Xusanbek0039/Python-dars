a = float(input("Birinchi sonni kiriting: "))
b = float(input("Ikkinchi sonni kiriting: "))

# Qo'shish
summa = a + b
print(f"{a} + {b} = {summa}")

# Ayirish 
ayirma = a - b
print(f"{a} - {b} = {ayirma}")

# Ko'paytirish
kopaytma = a * b 
print(f"{a} * {b} = {kopaytma}")

# Bo'lish
bolinma = a / b
print(f"{a} / {b} = {bolinma}")